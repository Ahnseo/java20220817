package ch01.book;

public class RunStatementExample {
	public static void main(String[] args) { 
		//단축기 : main + ctrl + space
		//실행문과 세미콜론
		int x = 1; // x 변수 선언하고 1을 저장
		int y = 2; // y 변수 선언하고 2를 저장
		int result = x + y ; //result 변수 선언하고 x 와 y를 더한 값 저장 
		System.out.println(x + "+" +  y + "=" + result); // 콘솔에 출력하는 메소드 호출
	}
}
